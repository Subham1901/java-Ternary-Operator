public class TernaryOp{
	public static void main(String[] args){
		int a=10;
		int b=12;
		
		int Result= (b > a)? 12:10;		//ternary operator at first it will check condition
							//then if condition gets true it will print 1st value(true)
							//if condition gets false it will print 2nd value(false).
		System.out.println(Result);
	}
}
